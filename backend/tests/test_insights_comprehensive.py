# ─── Helpers ─────────────────────────────────────────────────────────────────

PASS = "✅ PASS"
FAIL = "❌ FAIL"
SEP  = "─" * 65

def check(cond: bool, label: str) -> None:
    icon = PASS if cond else FAIL
    print(f"  {icon}  {label}")
    if not cond:
        raise AssertionError(label)


def section(title: str) -> None:
    print(f"\n{'═'*65}")
    print(f"  {title}")
    print('═'*65)


def subsection(title: str) -> None:
    print(f"\n  {SEP}")
    print(f"  {title}")
    print(f"  {SEP}")


