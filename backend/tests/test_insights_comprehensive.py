    # Prove decay: Phase 4 data alone should give "Exploring Ahead"
    subsection("Decay effect on Cognition (Phase 4 only)")
    start_date_400 = datetime.date(2025, 1, 1)
    phase4_start = (start_date_400 + datetime.timedelta(days=330)).isoformat()
    phase4 = [e for e in entries if e.get("date", "") >= phase4_start and e.get("subject")]
