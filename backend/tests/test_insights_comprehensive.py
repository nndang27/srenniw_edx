# [H] DECAY WEIGHTING PROOF
# ─────────────────────────────────────────────────────────────────────────────

def test_decay_proof() -> None:
    section("[H] Decay Weighting Proof")
    REF = "2026-04-06"

    print("\n  Decay weight schedule (half-life 90 days):")
    for days_ago in [0, 7, 14, 30, 60, 90, 120, 180, 365]:
        d = (datetime.date.fromisoformat(REF) - datetime.timedelta(days=days_ago)).isoformat()
        w = decay_weight(d, REF, 90)
        bar = "█" * int(w * 20)
        print(f"    {days_ago:4d} days ago → weight={w:.4f}  {bar}")

    check(abs(decay_weight(REF, REF, 90) - 1.0) < 0.001, "Today's weight = 1.0")
    check(abs(decay_weight(
        (datetime.date.fromisoformat(REF) - datetime.timedelta(days=90)).isoformat(), REF, 90
    ) - 0.5) < 0.01, "90-days-ago weight ≈ 0.5 (half-life check)")
    check(decay_weight(
        (datetime.date.fromisoformat(REF) - datetime.timedelta(days=180)).isoformat(), REF, 90
    ) < 0.26, "180-days-ago weight < 0.26 (≈ 0.25)")

    # Prove that recent data dominates intelligence scores
    entries_old = [
        {"date": "2025-01-15", "subject": "Maths", "cognitiveLevel": 1,
         "emotion": "Anxious", "parent_note": "", "teacher_note": ""},
        {"date": "2025-01-16", "subject": "Maths", "cognitiveLevel": 1,
         "emotion": "Anxious", "parent_note": "", "teacher_note": ""},
        {"date": "2025-01-17", "subject": "Maths", "cognitiveLevel": 1,
         "emotion": "Anxious", "parent_note": "", "teacher_note": ""},
    ]
    entries_recent = [
        {"date": "2026-04-01", "subject": "Maths", "cognitiveLevel": 5,
         "emotion": "Excited", "parent_note": "", "teacher_note": ""},
        {"date": "2026-04-02", "subject": "Maths", "cognitiveLevel": 5,
         "emotion": "Excited", "parent_note": "", "teacher_note": ""},
        {"date": "2026-04-03", "subject": "Maths", "cognitiveLevel": 5,
         "emotion": "Excited", "parent_note": "", "teacher_note": ""},
    ]

    r_old = json.loads(calculate_multiple_intelligences.invoke({
        "entries_json": json.dumps(entries_old), "ref_date": REF,
    }))
    r_recent = json.loads(calculate_multiple_intelligences.invoke({
        "entries_json": json.dumps(entries_recent), "ref_date": REF,
    }))
    r_mixed = json.loads(calculate_multiple_intelligences.invoke({
        "entries_json": json.dumps(entries_old + entries_recent), "ref_date": REF,
    }))

    subsection("Decay dominance: 3 old (bloom=1) vs 3 recent (bloom=5)")
    print(f"  Old-only  Logical: {r_old['radar_data']['Logical']}")
    print(f"  Recent-only Logical: {r_recent['radar_data']['Logical']}")
    print(f"  Mixed (old+recent) Logical: {r_mixed['radar_data']['Logical']}")

    check(
        r_recent["radar_data"]["Logical"] > r_old["radar_data"]["Logical"],
        "Recent bloom=5 scores higher than old bloom=1"
    )
    check(
        abs(r_mixed["radar_data"]["Logical"] - r_recent["radar_data"]["Logical"]) < 20,
        "Mixed score stays close to recent (decay suppresses old data)"
    )


