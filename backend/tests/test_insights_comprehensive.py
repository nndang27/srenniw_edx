def test_note_scorer(entries: list[dict]) -> None:
    section("[A] Note Scorer — keyword dimensions")

    test_cases = [
        ("parent", "Bé hỏi tại sao bầu trời có màu xanh, tò mò khám phá cùng bố",
         "curiosity_index", 0.5),
        ("parent", "Bé chạy nhảy cả buổi chiều, không ngồi yên",
         "activity_level", 0.4),
        ("parent", "Bé nhường đồ chơi cho em, chơi cùng bạn rất vui",
         "social_engagement", 0.4),
        ("parent", "Bé tự làm bài mà không cần nhắc nhở, tập trung cao",
         "focus_depth", 0.4),
        ("parent", "Bé khóc vì không làm được toán, lo lắng",
         "emotion_sentiment", None),   # should be < 3.0
        ("teacher", "Excellent participation and great engagement in class today",
         "encouragement_level", 0.3),
        ("teacher", "Struggled with fractions, needs more support and practice",
         "difficulty_signal", 0.3),
    ]

    all_pass = True
    for note_type, note, dim, threshold in test_cases:
        result = score_note_fast(note, note_type)
        val = result.get(dim, 0)
        if dim == "emotion_sentiment" and threshold is None:
            ok = val < 3.0
        else:
            ok = val >= threshold
        icon = PASS if ok else FAIL
        print(f"  {icon}  {note_type}/{dim}: {val:.2f}  (note: {note[:50]}...)")
        if not ok:
            all_pass = False

    # Stats on pre-scored data in dataset
    parent_emotions = [
        e["parent_note_scores"]["emotion_sentiment"]
        for e in entries
        if e.get("parent_note_scores")
    ]
    print(f"\n  Emotion sentiment stats across {len(parent_emotions)} entries:")
    print(f"    mean={statistics.mean(parent_emotions):.2f}  "
          f"stdev={statistics.stdev(parent_emotions):.2f}  "
          f"min={min(parent_emotions):.2f}  max={max(parent_emotions):.2f}")
    check(statistics.mean(parent_emotions) > 2.5, "Mean parent sentiment should be positive (>2.5)")
    check(statistics.stdev(parent_emotions) > 0.3, "Sentiment should have variance across entries")


